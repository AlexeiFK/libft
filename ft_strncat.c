#include <string.h>

char	*ft_strncat(char *s1, char *s2, size_t n)
{
	int		i;
	int		j;
	int		nb;

	nb = n;
	i = 0;
	j = 0;
	while (s1[i] != '\0')
	{
		i++;
	}
	while (s2[j] != '\0' && j < nb)
	{
		s1[i] = s2[j];
		j++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
